public class literals{
    public static void main(String[] args){
        System.out.println("The literals printing");
        int age = 18;
        float cgpa = 9.8f;
        char damini = 'D';
        System.out.println(age);
        System.out.println(cgpa);
        System.out.println(damini);
        String str = "Damini";
        System.out.println(str);
    }
}